"use strict"

function Addition(x, y) {
    return x + y;
}
function Subtraction(x, y) {
    return x - y;
}
function Division(x, y) {
    return x / y;
}
function Multiplication(x, y) {
    return x * y;
}
var s = Addition(10, 5);
var v = Subtraction(10, 5);
var d = Division(10, 5);
var u = Multiplication(10, 5);
console.log(s);
console.log(v);
console.log(d);
console.log(u);